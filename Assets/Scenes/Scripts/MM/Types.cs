            Var_Tscr, //{ Род теплового граничного условия на поверхности червяка }
            n_Integr, //{ Число циклов интегрирования вдоль оси червяка }
            n_Graph;  //{ Число циклов вдоль оси червяка при его рисовании }

        public double
            T0_,    //{ Фиксированное значение температуры материала }
            Mu0_,   //{ Коэффициент консистенции при температуре T0_, кПа*с^n }
            b_,     //{ Степень влияния температуры на вязкость полимера, 1/K }
            m_,     //{ Индекс течения полимера }
            N_,     //{ Частота вращения червяка, об/мин }
            Ro_,    //{ Плотность полимерного материала, кг/м^3 }
            Ro_gran_,//{ Плотность пробки гранул, кг/м^3 }
            T_PL_,   //{ Температура плавления (или размягчения) полимера,
                         //гр.Цельсия }
            K_PL_ ,   //{ Скрытая теплота плавления полимера, кДж/кг }
            T_St,   //{ Начальная температура полимера, гр.Цельсия }
            a_T_,   //{ Коэффициент температуропроводности полимера, м^2/с }
            Lam_T_, //{ Коэффициент теплопроводности полимера, Вт/(м*К) }
            Lam_k,  //{Коэффициент теплопроводности материала корпуса,Вт/(м*К)}
            Lam_s,  //{Коэффициент теплопроводности материала червяка,Вт/(м*К)}
            Al_kor, //{Коэффициент теплоотдачи полимера к корпусу, Вт/(м^2*К)}
            Al_scr, //{Коэффициент теплоотдачи полимера к червяку, Вт/(м^2*К)}
            Del_s,  //{Толщина теплопередающей стенки червяка, мм}
            Al_W_s, //{Коэффициент теплоотдачи теплоносителя к червяку,
                        //Вт/(м^2*К)}
            T_W_s,  //{Температура теплоносителя в полости червяка, град.Ц. }
            a_W,    //{Коэффициент температуропроводности теплоносителя в полости
                        //червяка, м^2/с}
            Lam_W,  //{Коэффициент теплопроводности теплоносителя в полости
                        //червяка, Вт/(м*К)}
            Q_W_s,  //{Объемный расход теплоносителя, охлаждающего червяк,
                        //дм^3/мин}
            T_scr;  //{Альтернативная температура червяка, град.Ц.}

        public double[]
        Lam_kor,   //{ Координаты границ секций корпуса, мм }
        T_kor;     //{ Температуры секций корпуса, град.Цельсия }
    }

