    public struct SECT //= ARRAY[1..29] of RECORD          { ГЕОМЕТРИЯ СЕКЦИЙ ЧЕРВЯКА }
    {
        public int
            S_Type,                 //{ Тип секции: 1 - с нарезкой, 2 - гладкая }
            Order,                   //{ П о р я д к о в ы й  н о м е р  секции }
            n_cykle,               //{ Число циклов интегрирования вдоль секции }
            Monolit,        //{ П р и з н а к  монолитности (1) или рыхлости (0)}
            n_Line,                            //{ Число заходов винтовой линии }
            R_or_L,                      //{ Тип нарезки: 1 - правая, 2 - левая }
            p_W;      //{ Признак: 1 - возможность испарения; 2 - герметичность }

        public double
            D_st,            //{ Внутренний диаметр корпуса в начале секции, мм }
            D_fin,            //{ Внутренний диаметр корпуса в конце секции, мм }
            L_sect,                                        //{ Длина секции, мм }
            step_,                           //{ Шаг подъема винтовой линии, мм }
            H_st,           //{ Глубина винтового канала (или радиальный зазор) }
                                //{ в  н а ч а л е  секции, мм }
            H_fin,          //{ Глубина винтового канала (или радиальный зазор) }
                                //{ в  к о н ц е  секции, мм }
            e_st,                        //{ Толщина гребна в начале секции, мм }
            e_fin,                        //{ Толщина гребна в конце секции, мм }
            delta,                                     //{ Радиальнвй зазор, мм }
            d_Fi,      //{ Угол поворота секции по отношению к предыдущей, град.}
            W_a;               //{ Суммарная ширина осевых прорезей нарезки, мм }
    }

    public struct DATA_
    {
        public int
            nS_1,     //{ Число секций c нарезкой червяка }
            nS_2,     //{ Число гладких секций червяка }
